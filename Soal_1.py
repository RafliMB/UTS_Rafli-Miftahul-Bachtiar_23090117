bilangan_pertama = int(input("Masukkan bilangan pertama : "))
bilangan_kedua = int(input("Masukkan bilangan kedua : "))
print("")

penjumlahan = bilangan_pertama + bilangan_kedua
print(f"Hasil penjumlahan",bilangan_pertama,"dan",bilangan_kedua,"adalah",penjumlahan)

pengurangan = bilangan_pertama - bilangan_kedua
print(f"Hasil pengurangan",bilangan_pertama,"dan",bilangan_kedua,"adalah",pengurangan)

modulus = bilangan_pertama % bilangan_kedua
print(f"Hasil sisa pembagian",bilangan_pertama,"dan",bilangan_kedua,"adalah",modulus)